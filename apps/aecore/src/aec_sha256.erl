-module(aec_sha256).

-export([hash/1]).

-export_type([hashable/0]).

-include("sha256.hrl").

-type hashable() :: binary().

-type hash() :: <<_:256>>.

%%%=============================================================================
%%% API
%%%=============================================================================

%%------------------------------------------------------------------------------
%% Calculate the SHA256 hash value of a binary
%%------------------------------------------------------------------------------
-spec hash(hashable()) -> hash().
hash(Data) when is_binary(Data) ->
    <<_:?HASH_BYTES/unit:8>> = crypto:hash(sha256, Data).
